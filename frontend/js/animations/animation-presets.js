function getPreset(presetName) {
    return ANIMATION_PRESETS[presetName] || null;
}

function getPresetsByCategory(category) {
    return Object.entries(ANIMATION_PRESETS)
        .filter(([_, preset]) => preset.category === category)
        .map(([id, preset]) => ({ id, ...preset }));
}

function getAllPresets() {
    return Object.entries(ANIMATION_PRESETS).map(([id, preset]) => ({ id, ...preset }));
}

function createPresetAnimation(presetName, overrides = {}) {
    const preset = getPreset(presetName);
    if (!preset) return null;

    return {
        ...preset,
        ...overrides,
        id: generateId("anim"),
        trigger: overrides.trigger === "on-click" ? "on-click" : preset.trigger || "on-slide",
        delay: Math.max(0, Number(overrides.delay ?? preset.delay) || 0),
        duration: Math.max(100, Number(overrides.duration ?? preset.duration) || 600),
    };
}

